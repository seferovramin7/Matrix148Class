package org.matrix.Lesson_25;

public class ListNode {
    int val;
    int next;

    public ListNode(int val, int next) {
        this.val = val;
        this.next = next;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public int getNext() {
        return next;
    }

    public void setNext(int next) {
        this.next = next;
    }
}
